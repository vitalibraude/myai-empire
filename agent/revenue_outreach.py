def send_audit_email(to_email, to_name, website_url, creds):
    """Run audit on a prospect's site and send them the results."""
    print(f"[OUTREACH] Auditing {website_url} for {to_name}...")

    # Run audit
    report = run_audit(website_url)
    if "error" in report and "overall_score" not in report:
        print(f"[OUTREACH] Audit failed for {website_url}: {report.get('error')}")
        return False

    # Save HTML report
    safe_name = website_url.replace("https://", "").replace("http://", "").replace("/", "_")
    report_path = os.path.join(BASE_DIR, "output", "audits", f"audit_{safe_name}.html")
    generate_html_report(report, report_path)

    # Generate email
    subject, html_body = generate_outreach_email(to_name, website_url, report)

    # Send email
    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"] = f"myAI <{creds['email']}>"
    msg["To"] = to_email
    msg.attach(MIMEText(html_body, "html"))

    try:
        ctx = ssl.create_default_context()
        server = smtplib.SMTP(creds["smtp_host"], creds["smtp_port"], timeout=15)
        server.ehlo()
        server.starttls(context=ctx)
        server.ehlo()
        server.login(creds["email"], creds["password"])
        server.sendmail(creds["email"], to_email, msg.as_string())
        server.quit()

        print(f"[OUTREACH] Sent audit email to {to_email} (Score: {report['overall_score']})")

        # Log
        save_outreach_log({
            "to_email": to_email,
            "to_name": to_name,
            "website": website_url,
            "score": report["overall_score"],
            "grade": report["grade"],
            "issues_count": len(report["seo"]["issues"]),
            "sent_at": datetime.now().isoformat(),
            "status": "sent",
        })
        return True

    except Exception as e:
        print(f"[OUTREACH] Email failed to {to_email}: {e}")
        save_outreach_log({
            "to_email": to_email,
            "to_name": to_name,
            "website": website_url,
            "sent_at": datetime.now().isoformat(),
            "status": "failed",
            "error": str(e),
        })
        return False


def run_outreach_campaign(max_emails=5):
    """Run outreach campaign: audit prospect websites and send results."""
    creds = load_credentials()

    prospects = load_prospects()
    if not prospects:
        print("[OUTREACH] No prospects found. Add prospects to data/prospects.json")
        return

    # Load previous outreach log to avoid re-sending
    log_path = os.path.join(BASE_DIR, "data", "outreach_log.json")
    sent_emails = set()
    if os.path.exists(log_path):
        with open(log_path) as f:
            for entry in json.load(f):
                if entry.get("status") == "sent":
                    sent_emails.add(entry["to_email"])

    sent = 0
    for prospect in prospects:
        if sent >= max_emails:
            break

        email = prospect.get("email", "")
        name = prospect.get("name", prospect.get("business", "there"))
        website = prospect.get("website", "")

        if not email or not website:
            continue
        if email in sent_emails:
            print(f"[OUTREACH] Already emailed {email}, skipping")
            continue

        success = send_audit_email(email, name, website, creds)
        if success:
            sent += 1

    print(f"\n[OUTREACH] Campaign complete: {sent} emails sent out of {max_emails} max")


def get_outreach_stats():
    """Get outreach campaign statistics."""
    log_path = os.path.join(BASE_DIR, "data", "outreach_log.json")
    if not os.path.exists(log_path):
        return {"total": 0, "sent": 0, "failed": 0}

    with open(log_path) as f:
        logs = json.load(f)

    sent = sum(1 for l in logs if l.get("status") == "sent")
    failed = sum(1 for l in logs if l.get("status") == "failed")

    return {
        "total": len(logs),
        "sent": sent,
        "failed": failed,
        "logs": logs,
    }


if __name__ == "__main__":
    max_count = int(sys.argv[1]) if len(sys.argv) > 1 else 3
    run_outreach_campaign(max_emails=max_count)
