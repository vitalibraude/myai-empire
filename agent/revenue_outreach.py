"""
Revenue Outreach Engine — sends free audit reports to real businesses,
converts them into paying customers.

Flow:
1. Audit a prospect's website
2. Generate beautiful HTML report
3. Send personalized email with results + offer to fix
4. Track responses and conversions
"""
import os
import sys
import json
import smtplib
import ssl
from datetime import datetime
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart

BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, BASE_DIR)

from services.website_audit.audit_engine import run_audit, generate_html_report


def load_credentials():
    creds_path = os.path.join(BASE_DIR, "credentials.json")
    with open(creds_path) as f:
        raw = json.load(f)
    email_cfg = raw.get("email", raw)
    return {
        "email": email_cfg.get("from_email", email_cfg.get("username", email_cfg.get("email", ""))),
        "password": email_cfg.get("password", ""),
        "smtp_host": email_cfg.get("smtp_host", "smtp.gmail.com"),
        "smtp_port": int(email_cfg.get("smtp_port", 587)),
    }


def load_prospects():
    prospects_path = os.path.join(BASE_DIR, "data", "prospects.json")
    if os.path.exists(prospects_path):
        with open(prospects_path) as f:
            data = json.load(f)
        # Handle both flat list and dict-by-niche formats
        if isinstance(data, list):
            return data
        elif isinstance(data, dict):
            flat = []
            for niche, items in data.items():
                if isinstance(items, list):
                    for item in items:
                        item["niche"] = niche
                        flat.append(item)
            return flat
    return []


def save_outreach_log(log_entry):
    log_path = os.path.join(BASE_DIR, "data", "outreach_log.json")
    os.makedirs(os.path.dirname(log_path), exist_ok=True)
    logs = []
    if os.path.exists(log_path):
        with open(log_path) as f:
            logs = json.load(f)
    logs.append(log_entry)
    with open(log_path, "w") as f:
        json.dump(logs, f, indent=2)


